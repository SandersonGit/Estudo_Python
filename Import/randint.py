# O código usa o módulo `random` para gerar números aleatórios. A função `random.randint(1, 10)` gera um número aleatório entre 1 e 10 (inclusive). `random.randint(1, 1000)` gera um número aleatório entre 1 e 1000 (inclusive). Os resultados são impressos no console.

import random

print(random.randint(1,10))

aleatorio = random.randint(1,1000)
print(aleatorio)